package paquete1;

public class Clase1 {
	
	//Atributos privados
	private int edad;	
	private String nombre;
	
	//Metodo Setter: Establecemos la edad
	public void setEdad(int edad) {
		this.edad = edad;
	}
	//Metodos Getter: Mostramos la edad
	public int getEdad() {
		return edad;
	}
	
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getNombre() {
		return nombre;
	}
	
	
}
